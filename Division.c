// Program to find the ratio of numbers
#include<stdio.h>
int main()
{
    int a,b;
    float c;
    printf ("Enter first number=\n");
    scanf ("%d",&a);
    printf ("Enter second number=\n");
    scanf ("%d",&b);
    c= a/b ;
    printf ("Quotient when %d is divided by %d is = %f",a,b,c);
    return 0;
}