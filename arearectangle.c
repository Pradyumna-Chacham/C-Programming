// To find the area of a rectangle
#include<stdio.h>
int main()
{
    int l,b,area;
    printf ("Enter length of rectangle=\n");
    scanf ("%d",&l);
    printf ("Enter breadth of rectangle=\n");
    scanf ("%d",&b);
    area=l*b;
    printf ("Area of rectangle is=%d\n",area);
    return 0;
    
}